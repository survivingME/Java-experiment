public class GeometricObject {
    private String color;
    private boolean filled;

    public void setColor(String color1) {
        this.color = color1;
    }
    public void setFilled(boolean filled1) {
        this.filled = filled1;
    }
    public String getColor() {
        return this.color;
    }
    public boolean getFilled() {
        return this.filled;
    }
}
